    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        JFrame parentFrame=new JFrame();          //This opens a file chooser for the user
        JFileChooser fileChooser= new JFileChooser();    //Creates an object named fileChooser
        fileChooser.setDialogTitle("Choose where to save file");   //Title of the file chooser
        int response=fileChooser.showSaveDialog(parentFrame);    //Takes response from user(Save or Cancel)
        if(response==JFileChooser.APPROVE_OPTION){              //If the user clicks 'Save'
        File fileToSave= new File(fileChooser.getSelectedFile()+".doc");    //Adds a .doc to file name to create word document
        try{
        PrintWriter SaveStock= new PrintWriter(fileToSave);     //Creates a file in the directory 'fileToSave' to store following details
        SaveStock.println("The Debt ratio is "+DebtR);
        SaveStock.println("The P/E ratio is "+PE);
        SaveStock.println("The Current ratio is "+CurrR);
        SaveStock.println("The Revenue growth ratio is "+GrowthR);
        JOptionPane.showMessageDialog(null,"Information successfully saved to file","Saved Successfully",JOptionPane.INFORMATION_MESSAGE);
        SaveStock.close();                                      //Closes the connection to the file to avoid memory leak
        }catch(FileNotFoundException e){
             JOptionPane.showMessageDialog(null,"Could not save to file");
        }
        }
        if(response==JFileChooser.ABORT || response==JFileChooser.CANCEL_OPTION){   //If user clicks 'Cancel'
             JOptionPane.showMessageDialog(null,"Operation cancelled","Save Unsuccessfull",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed


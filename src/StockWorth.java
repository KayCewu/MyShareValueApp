    private void btnCalculateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCalculateMouseClicked
       try{
       double TotLiab,SP, TotAss,EPS, CurrentR, PrevR,CurrAss, CurrLiab ; //Variable declaration
       //The following code extracts the data from the input fields and converts it into the neccesary format for manipulation.
       TotLiab=Float.parseFloat(this.txtTotLiabilities.getText());
       TotAss=Float.parseFloat(this.txtTotAssets.getText());
       SP=Float.parseFloat(this.txtSharePrice.getText());
       EPS=Float.parseFloat(this.txtEPS.getText());
       CurrAss=Float.parseFloat(this.txtCAssets.getText());
       CurrLiab=Float.parseFloat(this.txtCLiabilities.getText());
       CurrentR=Float.parseFloat(this.txtCRev.getText());
       PrevR=Float.parseFloat(this.txtPRev.getText());
       DebtR=TotLiab/TotAss;
       DebtR=Math.round(DebtR*100.0)/100.0; //This is where we round the value off
       CurrR=CurrAss/CurrLiab;
       CurrR=Math.round(CurrR*100.0)/100.0; //This is where we round the value off
       PE=SP/EPS;
       PE=Math.round(PE*100.0)/100.0; //This is where we round the value off
       GrowthR=(CurrentR-PrevR)/PrevR*100;
       GrowthR=Math.round(GrowthR*100.0)/100.0; //This is where we round the value off
       if(DebtR>=0.5){
           this.txtDRatio.setText(" "+DebtR+" ,Bad");
       }if (DebtR<0.5){
           this.txtDRatio.setText(" "+DebtR+" ,Good");
       }if (CurrR>=1 && CurrR<=5){
            this.txtCurrentRatio.setText(" "+CurrR+" ,Good");
       }if (CurrR>5){
           this.txtCurrentRatio.setText(" "+CurrR+" ,Great");
       }if (CurrR<1){
           this.txtCurrentRatio.setText(" "+CurrR+" ,Bad");
       }if (PE<=10){
           this.txtPriceEarnings.setText(" "+PE+" ,Underpriced");
       }if (PE>10 && PE<20){
           this.txtPriceEarnings.setText(" "+PE+" ,Fairly Priced");
       }if (PE>=20){
           this.txtPriceEarnings.setText(" "+PE+" ,Overpriced");
       }if (GrowthR>=3){
           this.txtRevG.setText(" "+GrowthR+" ,Good");
       }if (GrowthR<=1){
           this.txtRevG.setText(" "+GrowthR+" ,Bad");
       }
       
       }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(null,"Check input for validity","Error",JOptionPane.ERROR_MESSAGE);
           this.txtCAssets.setText("");
           this.txtCLiabilities.setText("");
           this.txtCRev.setText("");
           this.txtDRatio.setText("");
           this.txtCurrentRatio.setText("");
           this.txtEPS.setText("");
           this.txtPRev.setText("");
           this.txtPriceEarnings.setText("");
           this.txtRevG.setText("");
           this.txtSharePrice.setText("");
           this.txtTotAssets.setText("");
           this.txtTotLiabilities.setText("");
       }
    }//GEN-LAST:event_btnCalculateMouseClicked

